#include "ft_printf.h"

int	ft_put_unsg(unsigned int n)
{
	int	i;

	i = 0;
	while (n > 9)
	{
		i += ft_put_unsg(n / 10);
		n %= 10;
	}
	i += ft_putchar(n + 48);
	return (i);
}
