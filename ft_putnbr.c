#include "ft_printf.h"

int	ft_putnbr(int nbr)
{
	long	n;
	int		i;

	i = 0;
	n = nbr;
	if (n < 0)
	{
		i++;
		n *= -1;
		ft_putchar('-');
	}
	i += ft_put_unsg(n);
	return (i);
}
